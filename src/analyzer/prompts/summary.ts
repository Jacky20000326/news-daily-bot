import { NewsItem, AnalyzedNewsItem } from '../../types';

// ─── 單則新聞摘要 Prompt ───────────────────────────────────────────────────────

/**
 * 建立單則新聞的繁體中文摘要 Prompt
 *
 * 要求：
 * - 100-150 字的繁體中文摘要
 * - 包含事件核心、市場影響、重要數據
 * - 語氣客觀專業
 */
export function buildSummaryPrompt(item: NewsItem): string {
  return `你是一位專業的加密貨幣財經記者，請為以下新聞撰寫一段繁體中文摘要。

## 摘要要求

- 字數：100 至 150 字（繁體中文字數，不含標點符號）
- 語言：繁體中文，用語客觀專業
- 內容必須涵蓋：
  1. 事件核心：發生了什麼事、涉及哪些主體
  2. 市場影響：對加密貨幣市場或特定幣種的潛在影響
  3. 重要數據：如有提及具體數字、百分比、金額等，必須納入
- 不得出現主觀評論或投資建議
- 直接輸出摘要內容，不需要標題或任何前綴說明

## 新聞資訊

**標題：** ${item.title}

**來源：** ${item.sourceName}

**發布時間：** ${item.publishedAt.toISOString()}

**內容：**
${item.content.slice(0, 1000)}

請直接輸出 100-150 字的繁體中文摘要：`;
}

// ─── 今日市場總覽 Prompt ──────────────────────────────────────────────────────

/**
 * 建立「今日市場總覽」的執行摘要 Prompt
 *
 * 要求：
 * - 250-300 字的繁體中文整體摘要
 * - 包含最重要 1-2 件大事
 * - 描述市場整體情緒
 * - 點出值得關注的趨勢或風險
 */
export function buildExecutiveSummaryPrompt(topItems: AnalyzedNewsItem[]): string {
  // 準備精簡的新聞清單供 AI 參考
  const newsList = topItems
    .map(
      (item, index) =>
        `${index + 1}. [重要度 ${item.importanceScore}/10 | ${item.category} | ${item.sentiment}]\n   標題：${item.title}\n   摘要：${item.aiSummary || item.content.slice(0, 200)}`
    )
    .join('\n\n');

  return `你是一位專業的加密貨幣市場分析師，請根據以下今日重要新聞撰寫一份「今日市場總覽」。

## 撰寫要求

- 字數：250 至 300 字（繁體中文字數，不含標點符號）
- 語言：繁體中文，語氣客觀專業
- 結構（請依序撰寫，無需標示章節標題）：
  1. **重大事件**：點出今日最重要的 1-2 件大事及其市場意義
  2. **市場情緒**：綜合描述今日整體市場情緒（偏多、偏空、中性分歧等）
  3. **趨勢與風險**：指出值得持續關注的市場趨勢或潛在風險
- 不得出現主觀投資建議
- 可引用具體數字或事件名稱增加可信度
- 直接輸出總覽內容，不需要標題或任何前綴說明

## 今日重要新聞（依重要度排序）

${newsList}

請直接輸出 250-300 字的繁體中文「今日市場總覽」：`;
}
