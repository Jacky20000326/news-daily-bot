import { AnalyzedNewsItem } from '../../types';

/**
 * 建立深度分析 Prompt — 基於實際文章全文生成繁體中文深度報告
 */
export function buildDeepAnalysisPrompt(
  item: AnalyzedNewsItem,
  articleContent: string,
): string {
  return `你是一位資深加密貨幣市場分析師，擁有豐富的區塊鏈產業知識與財經分析經驗。
請根據以下新聞的「原始文章全文」進行深度分析，撰寫一份繁體中文的專業分析報告。

## 分析報告要求

- 字數：400 至 600 字（繁體中文字數）
- 語言：繁體中文，語氣客觀專業
- 請依以下結構撰寫，每段以對應標題開頭：

**【事件概述】**
用 2-3 句話精確描述事件核心，包含關鍵主體、行為與背景。

**【深度解析】**
分析事件的深層意義與脈絡：
- 事件發生的背景與原因
- 涉及的技術或商業邏輯
- 引用文章中的關鍵數據、金額、百分比等具體資訊

**【市場影響】**
評估對加密貨幣市場的短期與中長期影響：
- 對相關幣種（${item.relatedTickers.length > 0 ? item.relatedTickers.join('、') : '主流幣種'}）的價格影響
- 對產業生態或投資人信心的影響
- 可能引發的連鎖效應

**【後續觀察重點】**
列出 2-3 個值得持續追蹤的觀察指標或事件發展方向。

## 注意事項
- 必須基於文章實際內容進行分析，不得憑空捏造資訊
- 如文章內容不足以支撐深度分析，請在可分析的範圍內盡量撰寫
- 不得出現主觀投資建議（如「建議買入/賣出」）
- 直接輸出分析報告，不需要額外的前綴說明

## 新聞基本資訊

**標題：** ${item.title}
**來源：** ${item.sourceName}
**AI 初步摘要：** ${item.aiSummary || '無'}
**分類：** ${item.category}
**情緒傾向：** ${item.sentiment}
**相關幣種：** ${item.relatedTickers.join(', ') || '無'}

## 原始文章全文

${articleContent.slice(0, 6000)}

請直接輸出繁體中文深度分析報告：`;
}
